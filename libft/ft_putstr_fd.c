#include "libft.h"

void		ft_putstr_fd(char const *str, int fd)
{
	size_t		s;

	s = 0;
	if (str)
	{
		while (str[s] != '\0')
		{
			ft_putchar_fd(str[s], fd);
			s++;
		}
	}
}
