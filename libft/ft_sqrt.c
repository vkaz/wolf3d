int		ft_sqrt(int nb)
{
	int r;

	r = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while ((r * r) < nb)
	{
		r++;
	}
	if ((nb % r) == 0)
	{
		return (r);
	}
	else
	{
		return (r);
	}
}
